nonasciiwhitespaces = [
  0x1680, 0x2000, 0x2001, 0x2002, 0x2003, 0x2004, 0x2005, 0x2006, 0x2007,
  0x2008, 0x2009, 0x200a, 0x202f, 0x205f, 0x3000, 0xfeff,
];

function identify(curChar) {
  return (
    curChar === 0x20 ||
    curChar === 0x09 ||
    curChar === 0x0b ||
    curChar === 0x0c ||
    curChar === 0xa0 ||
    (curChar >= 0x1680 && nonasciiwhitespaces.indexOf(curChar) >= 0)
  );
}
